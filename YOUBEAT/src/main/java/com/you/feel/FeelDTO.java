package com.you.feel;

public class FeelDTO {
	private int feelnum;
	private String feeltitle;
	private String feelartist;
	private String feeling;
	
	public int getFeelnum() {
		return feelnum;
	}
	public void setFeelnum(int feelnum) {
		this.feelnum = feelnum;
	}
	public String getFeeltitle() {
		return feeltitle;
	}
	public void setFeeltitle(String feeltitle) {
		this.feeltitle = feeltitle;
	}
	public String getFeelartist() {
		return feelartist;
	}
	public void setFeelartist(String feelartist) {
		this.feelartist = feelartist;
	}
	public String getFeeling() {
		return feeling;
	}
	public void setFeeling(String feeling) {
		this.feeling = feeling;
	}
}
