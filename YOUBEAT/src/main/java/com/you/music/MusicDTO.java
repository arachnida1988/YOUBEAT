package com.you.music;

public class MusicDTO {
	private int mnum;
	private int anum;
	private String mtitle;
	private String mgenre;
	private String mdate;
	private int mprice;
	private int mcounts;
	private int mavgpoint;
	
	public int getMnum() {
		return mnum;
	}
	public void setMnum(int mnum) {
		this.mnum = mnum;
	}
	public int getAnum() {
		return anum;
	}
	public void setAnum(int anum) {
		this.anum = anum;
	}
	public String getMtitle() {
		return mtitle;
	}
	public void setMtitle(String mtitle) {
		this.mtitle = mtitle;
	}
	public String getMgenre() {
		return mgenre;
	}
	public void setMgenre(String mgenre) {
		this.mgenre = mgenre;
	}
	public String getMdate() {
		return mdate;
	}
	public void setMdate(String mdate) {
		this.mdate = mdate;
	}
	public int getMprice() {
		return mprice;
	}
	public void setMprice(int mprice) {
		this.mprice = mprice;
	}
	public int getMcounts() {
		return mcounts;
	}
	public void setMcounts(int mcounts) {
		this.mcounts = mcounts;
	}
	public int getMavgpoint() {
		return mavgpoint;
	}
	public void setMavgpoint(int mavgpoint) {
		this.mavgpoint = mavgpoint;
	}
}
